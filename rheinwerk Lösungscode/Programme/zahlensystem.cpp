#include <iostream>
using namespace std;
int main()
{
   int a = 42;
   cout << "Oktal: " << oct << a << endl;
   cout << "Hexadezimal: " << hex << a << endl;
   cout << "Dezimal: " << dec << a << endl;

   cout << "052 = " << 052 << endl;
   cout << "0x2a = " << 0x2a << endl;
   cout << "0b101010 = " << 0b101010 << endl;
}
